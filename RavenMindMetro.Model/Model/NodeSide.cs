namespace RavenMind.Model
{
    /// <summary>
    /// Defines at which side the node is located.
    /// </summary>
    public enum NodeSide
    {
        /// <summary>
        /// The side is not specified.
        /// </summary>
        Undefined,
        /// <summary>
        /// The node is located at the left side.
        /// </summary>
        Left,
        /// <summary>
        /// The node is located at the right side.
        /// </summary>
        Right
    }
}
